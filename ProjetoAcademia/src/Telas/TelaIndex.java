    private void txtPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisaKeyReleased
        // TODO add your handling code here:
        
        try {
            String sql = "SELECT * FROM clientes WHERE Nome LIKE '%"+txtPesquisa.getText()+"%'";
            pst = conexao.prepareStatement(sql, ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
            rs = pst.executeQuery();
            rs.last();
            int row=rs.getRow();
            int col=rs.getMetaData().getColumnCount();
            String rowData[][]=new String[row][col];
            rs.beforeFirst();
            int r=0;
            while(rs.next()){
                for(int i=0;i<col;i++){
                    rowData[r][i] = rs.getString(i+1);
                }
                r++;
            }
            String[] NomeColuna={"CPF","Nome","TelefoneZap","TelefoneEme","Rua","Bairro","DataVencimento"};
            DefaultTableModel model = (DefaultTableModel)tbTabela.getModel();
            model.setDataVector(rowData, NomeColuna);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_txtPesquisaKeyReleased

