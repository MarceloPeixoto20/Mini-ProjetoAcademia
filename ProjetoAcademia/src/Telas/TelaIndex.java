    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        String cpf = tbTabela.getValueAt(tbTabela.getSelectedRow(), 0).toString();
        Date data = null;
        String txtdata = txtClienteDataven.getText();        
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            data = sdf.parse(txtdata);            
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Formato de data inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dataFormatada = dateFormat.format(data);        
        
        try {
            boolean b = pst.execute("UPDATE clientes set Nome='" + txtClienteNome.getText() + "', TelefoneZap='" + txtClienteTelzap.getText() + "', TelefoneEme='" + txtClienteTeleme.getText() + "', Rua='" + txtClienteRua.getText() + "', Bairro='" + txtClienteBairro.getText() + "', DataVencimento='" + dataFormatada + "' WHERE CPF='" + txtClienteCpf.getText() + "'");
            formWindowOpened(null);
            if(!b){
                JOptionPane.showMessageDialog(this,"Editado");
            }else{
                JOptionPane.showMessageDialog(this,"Erro, Tente de novo");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

